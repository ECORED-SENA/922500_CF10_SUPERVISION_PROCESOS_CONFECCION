              p Durante la ejecución del estudio, es necesario obtener y registrar toda la información que resulte a través de la observación directa del proceso estudiado. También es necesario que durante la intervención se descomponga la tarea en elementos, si estos llegan a sobrepasar tiempos de 15 segundos, y describir claramente el inicio y fin en el formato de estudio de tiempos. Dependiendo de la técnica a usar dentro del estudio, se debe proceder a cronometrar o filmar las actividades, teniendo en cuenta la cantidad de observaciones que requiere el estudio. Luego de esto, se debe registrar la información que se observó, para luego realizar los cálculos de los tiempos observados. 
